package HomeWorks.HomeWork23;

public class Calculator {
    
    public double addNumbers(double num1, double num2){
        return num1 + num2;
    }

    public double substrNumbers(double num1, double num2){
        return num1 - num2;
    }

    public double addNumbers(double num1, double num2, double num3){
        return num1 + num2 + num3;
    }
    public double substrNumbers(double num1, double num2, double num3){
        return num1 - num2 - num3;
    }
}
